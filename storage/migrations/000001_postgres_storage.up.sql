BEGIN
	IF EXISTS (
		SELECT 1
		FROM information_schema.columns
		WHERE table_schema = current_schema()
		  AND table_name = 'uploaded_files'
		  AND column_name = 'payload'
	) THEN
		EXECUTE $sql$
			INSERT INTO file_rows (
				file_id,
				position,
				row_number,
				values,
				is_valid,
				errors,
				search_text
			)
			SELECT
				uf.id,
				row_data.position::integer,
				GREATEST(1, COALESCE(NULLIF(uf.payload->>'headerRow', '')::integer, 1) + row_data.position::integer),
				row_data.row_values,
				invalid.errors IS NULL,
				COALESCE(invalid.errors, '[]'::jsonb),
				COALESCE((
					SELECT string_agg(field.value, ' ' ORDER BY field.key)
					FROM jsonb_each_text(row_data.row_values) AS field
				), '')
			FROM uploaded_files AS uf
			CROSS JOIN LATERAL jsonb_array_elements(COALESCE(uf.payload->'rows', '[]'::jsonb))
				WITH ORDINALITY AS row_data(row_values, position)
			LEFT JOIN LATERAL (
				SELECT invalid_row->'errors' AS errors
				FROM jsonb_array_elements(COALESCE(uf.payload->'invalidRows', '[]'::jsonb)) AS invalid_row
				WHERE NULLIF(invalid_row->>'row', '')::integer =
					COALESCE(NULLIF(uf.payload->>'headerRow', '')::integer, 1) + row_data.position::integer
				LIMIT 1
			) AS invalid ON TRUE
			WHERE uf.payload IS NOT NULL
			ON CONFLICT (file_id, position) DO NOTHING
		$sql$;
	END IF;
END
