CREATE TABLE IF NOT EXISTS contact_sources (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	contact_id TEXT NOT NULL REFERENCES contacts(id) ON DELETE RESTRICT,
	file_id TEXT REFERENCES uploaded_files(id) ON DELETE SET NULL,
	row_number INTEGER NOT NULL DEFAULT 0,
	action TEXT NOT NULL,
	incoming JSONB NOT NULL DEFAULT '{}'::jsonb,
	created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
	CONSTRAINT chk_contact_sources_row_number CHECK (row_number >= 0),
	CONSTRAINT chk_contact_sources_action CHECK (
		action IN ('created', 'matched', 'skipped', 'replaced', 'merged', 'fixed')
	),
	CONSTRAINT chk_contact_sources_incoming CHECK (jsonb_typeof(incoming) = 'object'),
	CONSTRAINT uq_contact_sources_event UNIQUE (contact_id, file_id, row_number, action)
);

CREATE INDEX IF NOT EXISTS contact_sources_file_id_idx
	ON contact_sources (file_id, row_number);
CREATE INDEX IF NOT EXISTS contact_sources_contact_id_idx
	ON contact_sources (contact_id, created_at DESC);

